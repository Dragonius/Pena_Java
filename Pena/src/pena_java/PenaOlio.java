package pena_java;

//PenaOlio
public class PenaOlio {
    private int rahat;
    private int eeppisyys;
    private int aika;
    
    public PenaOlio(){
        rahat = 1000;
        eeppisyys = 0;
        aika = 0;
    }
    
    

    public int getRahat() {
        return rahat;
    }

    public void setRahat(int rahat) {
        this.rahat = rahat;
    }

    public int getEeppisyys() {
        return eeppisyys;
    }

    public void setEeppisyys(int eeppisyys) {
        this.eeppisyys = eeppisyys;
    }

    public int getAika() {
        return aika;
    }

    public void setAika(int aika) {
        this.aika = aika;
    }
    
    
}
